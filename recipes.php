		<div class="row">
			<div class="col-lg-4">
				<img class="sec1-img1" src="https://i.imgur.com/TYuhKkT.jpg">

			</div>
			<div class="col-lg-8 sec1-p1">
				<h3>Ingredients: </h3>
					<ul>
						<li>250 g water</li>
						<li>150 g bubbly, sourdough starter</li>
						<li>25 g olive oil</li>
						<li>500 g bread flour</li>
						<li>10 g fine sea salt</li>
					</ul>
			</div>
		</div>

			<p>
			1- Whisk the ingredients in a large bowl,Squish the mixture together with your hands until the flour is fully absorbed. The dough will feel dry, rough and shaggy.Cover the bowl with plastic wrap and let it rest for about 30 minutes. After the dough has rested, work it into a ball directly in the bowl</p>
			<p>
				2- Now the dough is ready to rise.Cover the bowl and let rise at room temperature, about 68-70 F.The dough is ready when it has doubled in size and no longer looks dense.</p>
			<p>
			3- Remove the dough onto a lightly floured surface.Cut the dough in half to make 2 loaves, or leave it whole for a single loaf.</p>
			<p>
			4- Choose a Baking Pot</p>
			<p>
			5- After shaping the dough, it needs to rise again.</p>
			<p>
			6- After the second rise, and right before the dough goes into the oven, make a slash about 2-3 inches long down the center of the dough.This allows the steam to escape and for the dough to expand during baking.</p>
			<p>
			7- Place the lid on top of the pot and reduce the oven temperature to 400º F/ 204º C.Bake on the center rack for 20 minutes.When 20 minutes is up, remove the lid. Your bread will be pale and shiny like the picture above.Continue to bake (uncovered) for an additional 40 minutes or until deep, golden brown. The internal temperature should read 205-210º F/ 96-98º C.Cool on a wire rack for at least a 1 hour before slicing.</p>
