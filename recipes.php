			<h1>Molten Chocolate Cakes</h1>
			<div class="row">
				<div class="col-lg-4">
					<img class="sec1-img1" src="https://imagesvc.meredithcorp.io/v3/mm/image?url=https%3A%2F%2Fstatic.onecms.io%2Fwp-content%2Fuploads%2Fsites%2F9%2F2013%2F12%2F06%2Fmolten-chocolate-cake-FT-RECIPE0220.jpg">

				</div>
				<div class="col-lg-8">
					<h3>Ingredients: </h3>
						<ul>
							<li>1 stick (115 g) unsalted butter</li>
							<li>170 g bittersweet chocolate</li>
							<li>2 eggs</li>
							<li>2 egg yolks</li>
							<li>1/4 cup sugar</li>
							<li>Pinch of salt</li>
							<li>2 tablespoons all-purpose flour</li>
						</ul>
				</div>
			</div>

				<p>
				1- Preheat the oven to 450°. Butter and lightly flour four 6-ounce ramekins. Tap out the excess flour. Set the ramekins on a baking sheet.</p>
				<p>
				2- In a double boiler, over simmering water, melt the butter with the chocolate. In a medium bowl, beat the eggs with the egg yolks, sugar and salt at high speed until thickened and pale.</p>
				<p>
				3- Whisk the chocolate until smooth. Quickly fold it into the egg mixture along with the flour. Spoon the batter into the prepared ramekins and bake for 12 minutes, or until the sides of the cakes are firm but the centers are soft. Let the cakes cool in the ramekins for 1 minute, then cover each with an inverted dessert plate. Carefully turn each one over, let stand for 10 seconds and then unmold. Serve immediately</p>
		</div>


		<div class="section1">
			<h1>Margherita Pizza</h1>
			<div class="row">
				<div class="col-lg-4">
					<img class="sec1-img1" src="https://www.dominos.com.au/ManagedAssets/AU/product/P301/AU_P301_en_hero_4055.jpg?v-1013096145">
				</div>
				<div class="col-lg-8">
					<h3>Ingredients: </h3>
						<ul>
							<li>2½ cups (300 g) unbleached all-purpose flour</li>
							<li>1 teaspoon granulated sugar</li>
							<li>½ teaspoon active dry yeast</li>
							<li>¾ teaspoon kosher salt</li>
							<li>7 ounces (105°F to 115°F) warm water</li>
							<li>1 tablespoon extra virgin olive oil</li>
							<li>semolina and all-purpose flour</li>
							<li>1 cup pureed or crushed canned tomatoes</li>
							<li>2-3 fresh garlic cloves </li>
							<li>1 teaspoon extra virgin olive oil</li>
							<li>2-3 large pinches of kosher salt</li>
							<li>¼ teaspoon freshly ground black pepper</li>
							<li>7 ounces fresh mozzarella cheese</li>
							<li>5-6 large fresh basil leaves</li>
							<li>dried red pepper flakes (optional)</li>
						</ul>
				</div>
			</div>


				<p>1- Prepare Pizza Dough: In a medium bowl, whisk together the all-purpose flour, sugar, yeast and salt. Add the warm water and olive oil, and stir the mixture with a wooden spoon until the dough just begins to come together. It will seem shaggy and dry, but don’t worry.</p>
				<p>2- Scrape the dough onto a well-floured counter top and knead the dough for three minutes. It should quickly come together and begin to get sticky. Dust the dough with flour as needed (sometimes I will have to do this 2 to 3 times, depending on humidity levels) – it should be slightly tacky, but should not be sticking to your counter top.  After about 3 minutes, the dough should be smooth, slightly elastic, and tacky. Lightly grease a large mixing bowl with olive oil, and place the dough into the bowl.</p>
				<p>3-Cover the bowl with a kitchen towel (or plastic wrap) and allow the dough to rise in a warm, dry area of your kitchen for 2 hours or until the dough has doubled in size. Proofing Tip: If your kitchen is very cold, heat a large heatproof measuring cup of water in the microwave for 2 to 3 minutes. This creates a nice warm environment. Remove the cup and place the bowl with the dough in the microwave until it has risen.</p>
				<p>4- Preheat Oven and Pizza Steel or Stone: Place the pizza steel (or stone) on the second to top rack of your oven (roughly 8 inches from the broiler element), and preheat the oven and steel (or stone) to 550°F (285°C) for a minumum of 1 hour. If your oven does not go up to 550°F (285°C) or you are using a delicate pizza stone, I recommend heating it to a maximum of 500°F (260°C)</p>
				<p>5- As the oven is preheating, assemble the ingredients. In a small bowl, stir together the pureed tomatoes, minced garlic, extra virgin olive oil, pepper, and salt. Set aside another small bowl with the cubed mozzarella cheese (pat the cheese with a paper towel to remove any excess moisture). Set aside the basil leaves and grated parmigiano-reggiano cheese for easy grabbing.</p>
				<p>6- Separate the dough into two equal-sized portions. It will deflate slightly, but that is OK. Place the dough on a large plate or floured counter top, cover gently with plastic wrap, and allow the dough to rest for 5 to 10 minutes.</p>
				<p>7- Assemble the Pizza: Sprinkle the pizza peel (if you do not own a pizza peel, you can try using the back of a half sheet pan - but it is tricky!) with a tablespoon of semolina and dusting of all-purpose flour. Gently use both hands to stretch one ball of pizza dough into roughly a 10-inch circle (don’t worry if its not perfectly uniform). If the dough springs back or is too elastic, allow it to rest for an additional five minutes. The edges of the dough can be slightly thicker, but make sure the center of the dough is thin (you should be able to see some light through it if you held it up). Gently transfer the dough onto the semolina and flour dusted pizza peel or baking sheet.</p>
				<p>8- Drizzle or brush the dough lightly (using your fingertips) with olive oil (roughly a teaspoon. Using a large spoon, add roughly ½ cup of the tomato sauce onto the pizza dough, leaving a ½-inch or ¾-inch border on all sides. Use the back of the spoon to spread it evenly and thinly. Sprinkle a tablespoon of parmigiano-reggiano cheese onto the pizza sauce. Add half of the cubed mozzarella, distributing it evenly over the entire pizza. Using your hands, tear a few large basil leaves, and sprinkle the basil over the pizza. At this point, I’ll occasionally stretch the sides of the dough out a bit to make it even thinner. Gently slide the pizza from the peel onto the heated baking stone. Bake for 7 to 8 minutes, or until the crust is golden and the cheese is bubbling and caramelized and the edges of the pizza are golden brown.</p>
